/************************************************************************

	Function name	<< mcpGetFont >>
					ＬＥＤへ表示するフォントを展開する
	Syntax			mcpGetFont( void );
	Argument		none
	Author			TOA Corporation  K.Honda
	Revision		a00 2000/06/27

*************************************************************************/
void mcpGetFont( void )
{
		int		i,j,k;
		char	*p;

			/* ＲＡＷデータエリアへ設定 */
		if( sw_led!=now_sw_led ){
			now_sw_led = sw_led;
			for( i=0; i<16; i++ ){
				rawData[i][8] = (char)(sw_led & 0x0FF);
				rawData[i][9] = (char)((sw_led>>8) & 0x0FF);
			}
			watch_dog_reset();
		}
		if( sw_blink!=now_sw_blink ){
			now_sw_blink = sw_blink;

			for( i=0; i<16; i++ ){
				blinkPositionData[i][8] = (char)(sw_blink & 0x0FF);
				blinkPositionData[i][9] = (char)((sw_blink>>8) & 0x0FF);
			}
			watch_dog_reset();
		}

			/* マトリックスＬＥＤのＲＡＷデータエリアへの設定 */
		for( i=0; i<4; i++ ){
			for( j=0; j<6; j++ ){
				if( matrix_led[i][j]!=now_matrix_led[i][j] ){	/*文字列内容が変化しているとき */
					now_matrix_led[i][j] = matrix_led[i][j];

					p = (char *)font_data[matrix_led[i][j] & 0x7F];	/*文字のフォントデータを取り出し*/
					for( k=0; k<5; k++ ){					/* ＲＡＷデータとして展開する */
						if( j<3 ){
							rawData[5*j+k][4+i] = *p++;
						}
						else {
							rawData[5*(j-3)+k][0+i] = *p++;
						}
					}
				}
			}
			watch_dog_reset();
		}

		for( i=0; i<4; i++ ){
			if( matrix_blink[i]!=now_matrix_blink[i] ){
				now_matrix_blink[i] = matrix_blink[i];

				for( j=0; j<6; j++ ){
					if( matrix_blink[i] & (0x0001<<j) ){
						for( k=0; k<5; k++ ){
							if( j<3 ){
								blinkPositionData[5*j+k][4+i] = 0xFF;
							}
							else {
								blinkPositionData[5*(j-3)+k][0+i] = 0xFF;
							}
						}
					}
					else {
						for( k=0; k<5; k++ ){
							if( j<3 ){
								blinkPositionData[5*j+k][4+i] = 0x00;
							}
							else {
								blinkPositionData[5*(j-3)+k][0+i] = 0x00;
							}
						}
					}
				}
			}
			watch_dog_reset();
		}

}

/************************************************************************

	Function name	<< mcpBlinkCount >>
				点滅カウンターによって点灯・消灯の判定データを作成する
	Syntax			mcpBlinkCount( void );
	Argument		none
	Author			TOA Corporation  K.Honda
	Revision		a00 2000/06/27

*************************************************************************/
void mcpBlinkCount( void )
{
		if( blinkCount<25 ){		/*点灯タイミング*/
			blinkPositionCheck = 0x0ff;
		}
		else {						/*消灯タイミング*/
			blinkPositionCheck = 0x000;
		}

		blinkCount++;
		if( blinkCount>=50 ){		/*５００ｍＳカウントのチェック*/
			blinkCount=0;
		}
}

